package Chaper01;
/**
 * 문서화 주석
 * 코드의 문서화를 위해 사용
 * 
 * 클래스, 메서드, 필드 등에 대한 설명을 주로 담습니다.
 * 
 * */
public class HelloWorld {

	public static void main(String[] args) {
		System.out.println("Hello World!");

		// 한 줄 주석
		// // 슬래쉬 2개를 사용해서 주석을 작성 
		// 뒤의 모든 내용은 그 줄에서 주석으로 간주된다.
		
		int x = 10; // 변수 x에 10을 할당합니다.
		
		/*
		 * 여러 줄에 걸친
		 * 주석입니다.
		 * 
		 * */
		int y = 20;
		/*
		 * 변수 y에
		 * 20을 할당합니다.
		 * */
	}

}